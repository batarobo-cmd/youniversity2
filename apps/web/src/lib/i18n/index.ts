const catalogs: Record<Locale, Messages> = { sk, en, cs: sk, de: en, hu: sk };

export function t(key: string, loc: Locale, vars?: Record<string, string | number>): string {
  let text = catalogs[loc]?.[key] ?? catalogs.sk[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

const AUTH_ERROR_KEYS: Record<string, string> = {
  account_suspended: 'auth.accountSuspended',
  domain_not_allowed: 'auth.domainNotAllowed',
  registration_disabled: 'auth.registrationDisabled',
  manual_login_disabled: 'auth.manualLoginDisabled',
  oauth_disabled: 'auth.oauthDisabled',
  captcha_required: 'auth.captchaRequired',
  captcha_failed: 'auth.captchaFailed',
  captcha_unavailable: 'auth.captchaFailed',
  rate_limited: 'auth.rateLimited',
};

export function authErrorMessage(
  code: string | null | undefined,
  fallback: string | undefined,
  loc: Locale,
): string {
  if (code) {
    const key = AUTH_ERROR_KEYS[code];
    if (key) return t(key, loc);
  }
  return fallback ?? '';
}

export function clientErrorMessage(
  payload: { error?: string; code?: string } | null | undefined,
  status: number,
  loc: Locale,
): string {
  const fromCode = authErrorMessage(payload?.code, undefined, loc);
  if (fromCode) return fromCode;
  if (payload?.error) return payload.error;
  if (status === 401) return t('error.sessionExpired', loc);
  return t('error.httpStatus', loc, { status });
}

